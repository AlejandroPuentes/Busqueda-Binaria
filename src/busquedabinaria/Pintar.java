    public Pintar() {
        initComponents();
        int A[];
        int i,k;
        Random ra = new Random();
        BusquedaBinaria busq  = new BusquedaBinaria();
        int N=500;
        A = new int [N];
        for (i=1;i<N;i++){
            A[i]=i;
        }
        
      
        for (k = 1; k < 1000; k++) {
            busq.setConta(0);
            int pos = ra.nextInt(500);
            busq.Bi_recursiva(A,A[pos], A.length-1, 0);
            datosB.addValue(busq.getConta(), "BusquedaC ", " "+k); 
            double lg = Math.log10(k)/Math.log10(20)*40;
            datosB.addValue(lg, "Logaritmo ", " "+k);
        }
        
      
        Grafica = ChartFactory.createLineChart("Busqueda Binaria",
                "TamaÃ±o", "Contador", datosB,
                PlotOrientation.VERTICAL, true, true, false);
    }

